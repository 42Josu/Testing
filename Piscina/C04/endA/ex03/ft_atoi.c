int	tururu(int *i, char *str)
{
	while (i[2] <= i[0])
	{
		if (str[i[2]] >= '0' && str[i[2]] <= '9')
		{
			i[3] += (str[i[2]] - '0');
			if (str[i[2] + 1] >= '0' && str[i[2] + 1] <= '9')
				i[3] *= 10;
		}
		else
		{
			break ;
		}
		i[2]++;
	}
	if ((i[1] % 2) != 0)
		i[3] *= -1;
	return (i[3]);
}

int	ft_atoi(char *str)
{
	int	i[4];

	i[0] = 0;
	i[1] = 0;
	i[2] = 0;
	i[3] = 0;
	while (str[i[0]] != '\0')
	{
		i[0]++;
	}
	while (str[i[2]] == '-' || str[i[2]] == '+' || str[i[2]] == ' ')
	{
		if (str[i[2]] == '-')
			i[1]++;
		i[2]++;
	}
	return (tururu(i, str));
}
