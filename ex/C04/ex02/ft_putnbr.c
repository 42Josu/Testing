#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	a[256];
	int		i;
	
	i = 0;
	if(nb < 0)
	{
		write(1, "-", 1);
		if(nb == -2147483648)
		{
			write(1, "2", 1);
		}
		nb *= -1;
	}
	while(nb > 9)
	{
		a[i] = (nb % 10) + '0';
		nb /= 10;
		i++;
		
	}
	a[i] = nb + '0';
	while (i >= 0)
	{
		write(1, &a[i], 1);
		i--;
	}
}
