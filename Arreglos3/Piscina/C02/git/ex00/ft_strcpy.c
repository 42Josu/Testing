char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (dest[i])
	{
		i++;
	}
	while (src[n])
	{
		dest[i + n] = src[n];
		n++;
	}
	return (dest);
}
